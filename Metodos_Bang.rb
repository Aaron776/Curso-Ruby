# Los metodos bang son aquellos metodos que termina con el simbolo de exclamacion (!), estos metodos modifican nuestro objeto o nuestra variable con su dato, por ejemplo:

cadena="Hola Mundo desde Ruby"

#Ejemplo 1:
resultado=cadena.upcase!
puts resultado

#Ejemplo 2:
resultado2=cadena.downcase!
puts resultado2

#Ejemplo 3:
resultado3=cadena.capitalize!
puts resultado3

#Ejemplo 4:
resultado4=cadena.reverse!
puts resultado4