# Para los rangos hay diferentes metodos que podemos aplicar

rango=(1..5)

#Metodo .min: devuelve el valor minimo del rango de Numeros
puts rango.min

#Metodo .max: devuelve el valor maximo del rango de numero
puts rango.max

# Metodo .include?(numero): devuelve true si el numero esta dentro del rango o false si no esta en ese rangos
puts rango.include?(3)

