# Los operadores logicos son aquelos operadores que permiten hacer compraciones logicas entre dos o mas elementos y devuleven un true o false

#Menor que
resultado=5<3;
puts resultado;

#Mayor que
resultado1=5>3;
puts resultado1

#Menor o Igual
resultado2=5<=3;
puts resultado2


#Mayor o Igual
resultado3=5>=3
puts resultado3

#Igual a

resultado4=5==3
puts resultado4


#Diferente a
resultado5=5!=3
puts resultado5