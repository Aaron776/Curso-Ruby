#Un array es un conjunto de elementos, los arreglos funcionan atraves de posiciones o indeces de cada uno de los elementos
#Para crear un arrays debemos ponerle un nombre y agregar corchetes para delimitar mi conjunto de elementos

numeros=[1,2,3,4,5,6]
paises=["Ecuador","Mexico","Peru","Brasil"]
nombres=["Aaron","Dagmar","Rosario","Jose"]

#Imprimir un arreglo
puts numeros

#Para imprimir un solo elemento del arreglo los hacemos atraves de su posicion dentro de arreeglo y su posicion lo poenmos entre corchetes
puts paises[0] #aqui impirmo el elemento que esta en la posicion 0
puts nombres[1,2] #aqui impirmo los elementos que van desde el indice 1 al indice 2