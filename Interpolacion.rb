# La interpolacion es mezclar tipos de datos, para mezclar tipos de datos usamos los siguiente elementos: #{} y dentro de la llaves ponemos el valor o el nombre de la variable que tenga un dato

suma=2+6
resultado="El resultado de la suma es: #{suma}" #podemos interpolar cadenas de string con numeros, booleanos, arrays, etc

edad=23
nombre="Aaron Ortiz"
saludo="Mi nombre es #{nombre} y mi edad es #{edad}"

puts resultado
puts saludo