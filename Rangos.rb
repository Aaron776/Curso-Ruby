# Los rangos son una forma de iterar sobre una secuencia de numeros, por ejemplo:

# Rango de Numeros van entre parentesis ponemos el numero inicial y el final  y en medio poenmos dos puntos seguidos
puts (1..10)

#Un metodo para poider usados en los rangos es el .to_a que nos devuelve un arreglo con los numeros que estan dentro del rango
puts (1..10).to_a
puts (-10..-1).to_a